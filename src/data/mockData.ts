// Generate Daily Metrics
export const generateDailyMetrics = (): DailyMetrics[] => {
  const metrics: DailyMetrics[] = [];
  const today = new Date();
  
  for (let i = 29; i >= 0; i--) {
    const date = new Date(today);
    date.setDate(date.getDate() - i);
    const dateStr = date.toISOString().split('T')[0];
    
    metrics.push({
      date: dateStr,
      meetings: Math.floor(Math.random() * 8) + 2,
      oneOnOneMeetings: Math.floor(Math.random() * 5) + 1,
      groupMeetings: Math.floor(Math.random() * 3),
      samplesDistributed: Math.floor(Math.random() * 15) + 5,
      sales: Math.floor(Math.random() * 10) + 2,
      b2cSales: Math.floor(Math.random() * 6) + 1,
      b2bSales: Math.floor(Math.random() * 4) + 1,
      revenue: Math.floor(Math.random() * 50000) + 10000,
      distanceTraveled: Math.floor(Math.random() * 80) + 20,
      villagesVisited: ['Kharawar', 'Lahli', 'Doraha', 'Sanganer'].slice(0, Math.floor(Math.random() * 3) + 1),
    });
  }
  
  return metrics;
};

